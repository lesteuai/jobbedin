# Initialize once globally
web_search = TavilySearch(max_results=5)

@tool
def company_search(query: str) -> str:
    """Search for recent news, hiring trends, and work culture about a company."""

    # Run the searches
    do_results = web_search.invoke(f"What {query} do in 2025 2026?")
    hire_results = web_search.invoke(f"Why {query} are hiring right now?")
    culture_results = web_search.invoke(f"What {query} 's work culture?")

    # Extract JUST the text content, joined by spaces or newlines
    what_they_do = " ".join([res.get("content", "") for res in do_results.get("results", [])])
    why_they_hiring = " ".join([res.get("content", "") for res in hire_results.get("results", [])])
    work_culture = " ".join([res.get("content", "") for res in culture_results.get("results", [])])

    # Format into a clean string for the LLM
    results = (
        f"What they do: {what_they_do}\n"
        f"Why they are hiring: {why_they_hiring}\n"
        f"Work culture: {work_culture}"
    )

    return results

"""## Encode and Decode images"""

def extract_text_from_pdf(pdf_path: str) -> str:
    """Extracts and formats text from a PDF document."""
    extracted_text = ""

    try:
        # Open the document
        with fitz.open(pdf_path) as pdf_doc:
            for page_num in range(len(pdf_doc)):
                page = pdf_doc[page_num]
                # Extract text preserving the logical reading order as best as possible
                extracted_text += page.get_text("text") + "\n\n"

        return extracted_text.strip()

    except Exception as e:
        return f"Error reading PDF: {e}"

