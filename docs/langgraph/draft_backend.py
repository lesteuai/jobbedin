# State: shared memory passed between all agents
class AgentState(TypedDict):
    job: str                                            # The job being researched
    company: str					                              # The company being researched
    resume: str						                              # Turn pdf into string
    company_result: str                                 # Output from ResearchCompany
    JDMatch_result: str                                 # Output from CrossRef
    feedback_result: str                                # Output from ResumeFeedback
    cover_letter_result: str                            # Output from GenerateLetter
    msg_result: str					                            # Output from GenerateMsg


# Node functions: each runs one agent and updates state

def run_ResearchCompany(state: AgentState) -> dict:
    # This stays as an agent because it uses the company_search tool
    result = ResearchCompany.invoke(
        {"messages": [("user", f"Find information about {state['company']}")]}
    )
    return {"company_result": result["messages"][-1].content}

def run_ResumeFeedback(state: AgentState) -> dict:
    # Using the LCEL chain
    result_str = resume_feedback_chain.invoke({"resume": state['resume']})
    return {"feedback_result": result_str}

def run_CrossRef(state: AgentState) -> dict:
    # Using the LCEL chain
    result_str = crossref_chain.invoke({
        "job": state['job'],
        "resume": state['resume']
    })
    return {"JDMatch_result": result_str}

def run_GenerateLetter(state: AgentState) -> dict:
    # Using the LCEL chain
    result_str = generate_letter_chain.invoke({
        "JDMatch_result": state['JDMatch_result'],
        "company_result": state['company_result'],
        "resume": state['resume'],
        "job": state['job']
    })
    return {"cover_letter_result": result_str}

def run_GenerateMsg(state: AgentState) -> dict:
    # Using the LCEL chain
    result_str = generate_msg_chain.invoke({
        "JDMatch_result": state['JDMatch_result'],
        "company_result": state['company_result'],
        "resume": state['resume'],
        "job": state['job']
    })
    return {"msg_result": result_str}

# 1. Initialize the graph with your AgentState
workflow = StateGraph(AgentState)

# 2. Add all your configured nodes
workflow.add_node("ResearchCompany", run_ResearchCompany)
workflow.add_node("ResumeFeedback", run_ResumeFeedback)
workflow.add_node("CrossRef", run_CrossRef)
workflow.add_node("GenerateLetter", run_GenerateLetter)
workflow.add_node("GenerateMsg", run_GenerateMsg)

workflow.add_edge(START, "ResearchCompany")
workflow.add_edge(START, "ResumeFeedback")
workflow.add_edge(START, "CrossRef")

# Generate Letter and Msg wait for both Research and CrossRef to finish
# (LangGraph handles this automatically if you don't map them sequentially)
workflow.add_edge("ResearchCompany", "GenerateLetter")
workflow.add_edge("CrossRef", "GenerateLetter")

workflow.add_edge("ResearchCompany", "GenerateMsg")
workflow.add_edge("CrossRef", "GenerateMsg")

# End the graph
workflow.add_edge("ResumeFeedback", END)
workflow.add_edge("GenerateLetter", END)
workflow.add_edge("GenerateMsg", END)

app = workflow.compile()

